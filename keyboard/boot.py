import usb_hid
import usb_cdc

REPORT_DESC = bytes([
    0x06,0x00,0xFF,
    0x09,0x01,
    0xA1,0x01,
    0x85,0x04,
    0x15,0x00,
    0x26,0xFF,0x00,
    0x75,0x08,
    0x95,0x30,
    0x09,0x01,
    0x81,0x02,
    0x09,0x01,
    0x91,0x02,
    0xC0
])

hid = usb_hid.Device(
    report_descriptor=REPORT_DESC,
    usage_page=0xFF00,
    usage=0x01,
    report_ids=(0x04,),
    in_report_lengths=(48,),
    out_report_lengths=(48,),
)

usb_hid.enable((hid, usb_hid.Device.KEYBOARD))
